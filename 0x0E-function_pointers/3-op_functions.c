#include "3-calc.h"

/**
 * op_add - returns the sum of a and b
 *
 * @a: integer value
 * @b: integer value
 *
 * Return: sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - returns the difference of a and b
 *
 * @a: integer value
 * @b: integer value
 *
 * Return: difference
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - returns the product of a and b
 *
 * @a: integer value
 * @b: integer value
 *
 * Return: product
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - returns the results of the division of a by b
 *
 * @a: integer value
 * @b: integer value
 *
 * Return: results
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns the remainder of the division of a by b
 *
 * @a: integer value
 * @b: integer value
 *
 * Return: remainder
 */

int op_mod(int a, int b)
{
	return (a % b);
}
