/**
 * add - returns the sum of a and b
 *
 * @a: integer value
 * @b: integer value
 *
 * Return: sum
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - returns the difference of a and b
 *
 * @a: integer value
 * @b: integer value
 *
 * Return: difference
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - returns the product of a and b
 *
 * @a: integer value
 * @b: integer value
 *
 * Return: product
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - returns the results of the division of a by b
 *
 * @a: integer value
 * @b: integer value
 *
 * Return: results
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - returns the remainder of the division of a by b
 *
 * @a: integer value
 * @b: integer value
 *
 * Return: remainder
 */

int mod(int a, int b)
{
	return (a % b);
}
